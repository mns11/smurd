package com.github.smurd.bot;

import com.github.smurd.command.CommandContainer;
import com.github.smurd.repository.DrummerRepository;
import com.github.smurd.service.DrummerSubService;
import com.github.smurd.service.SendBotMessageServiceImpl;
import com.github.smurd.service.TelegramUserService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;
import org.telegram.telegrambots.bots.TelegramLongPollingBot;
import org.telegram.telegrambots.meta.api.objects.Update;

import static com.github.smurd.command.CommandName.NO;

/**
 * Telegram Bot for drummers.
 */
@Component
public class SmurdTelegramBot extends TelegramLongPollingBot {

    public static String COMMAND_PREFIX = "/";

    @Value("${bot.username}")
    private String username;

    @Value("${bot.token}")
    private String token;

    private final CommandContainer commandContainer;

    @Autowired
    public SmurdTelegramBot(TelegramUserService telegramUserService,
            DrummerSubService drummerSubService, DrummerRepository drummerRepository) {
        this.commandContainer = new CommandContainer(new SendBotMessageServiceImpl(
                this), telegramUserService, drummerSubService, drummerRepository);
    }

    @Override
    public void onUpdateReceived(Update update) {
        if (update.hasMessage() && update.getMessage().hasText()) {
            String message = update.getMessage().getText().trim();
            if (message.startsWith(COMMAND_PREFIX)) {
                String commandIdentifier = message.split(" ")[0].toLowerCase();

                commandContainer.retrieveCommand(commandIdentifier).execute(update);
            } else {
                commandContainer.retrieveCommand(NO.getCommandName()).execute(update);
            }
        }
    }

    @Override
    public String getBotUsername() {
        return username;
    }

    @Override
    public String getBotToken() {
        return token;
    }
}
